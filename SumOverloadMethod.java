class SumOverloadMethod
{
    public void add(int i, int j)
    {
        System.out.println(i+j);
    }

    public void add(int i, int j, int k)
    {
        System.out.println(i+j+k);
    }

    public void add(double i, double j)
    {
        System.out.println(i+j);
    }
}

